document.addEventListener('DOMContentLoaded', () => {
    // Smooth scrolling
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            document.querySelector(this.getAttribute('href')).scrollIntoView({
                behavior: 'smooth'
            });
        });
    });

    // Mobile Menu
    const hamburger = document.querySelector('.hamburger');
    const navLinks = document.querySelector('.nav-links');

    if (hamburger) {
        hamburger.addEventListener('click', () => {
            if (navLinks.style.display === 'flex') {
                navLinks.style.display = 'none';
            } else {
                navLinks.style.display = 'flex';
                // Inline styles for basic toggle, can be improved with classes
                navLinks.style.flexDirection = 'column';
                navLinks.style.position = 'absolute';
                navLinks.style.top = '80px';
                navLinks.style.right = '0';
                navLinks.style.backgroundColor = 'rgba(10, 25, 47, 0.95)';
                navLinks.style.width = '100%';
                navLinks.style.padding = '20px';
                navLinks.style.textAlign = 'center';
            }
        });
    }

    // Force scroll to top on refresh
    if (history.scrollRestoration) {
        history.scrollRestoration = 'manual';
    } else {
        window.onbeforeunload = function () {
            window.scrollTo(0, 0);
        }
    }
    window.scrollTo(0, 0);

    // Language Toggle logic continues...
    let currentLang = localStorage.getItem('lang') || 'tr';
    const langBtn = document.getElementById('lang-toggle');

    // Initial load
    updateLanguage(currentLang);

    langBtn.addEventListener('click', () => {
        currentLang = currentLang === 'tr' ? 'en' : 'tr';
        localStorage.setItem('lang', currentLang);
        window.location.reload();
    });

    function updateLanguage(lang) {
        if (lang === 'tr') {
            langBtn.textContent = 'EN';
            document.documentElement.lang = 'tr';
        } else {
            langBtn.textContent = 'TR';
            document.documentElement.lang = 'en';
        }

        // Update all text elements
        document.querySelectorAll('[data-i18n]').forEach(element => {
            const key = element.getAttribute('data-i18n');
            if (translations[lang][key]) {
                element.textContent = translations[lang][key];
            }
        });

        // Trigger documentation module reload if it exists on the page
        if (typeof window.reloadDocLanguage === 'function') {
            window.reloadDocLanguage();
        }
    }

    // Clickable Cards logic
    const handleCardClick = (selector) => {
        document.querySelectorAll(selector).forEach(card => {
            const link = card.querySelector('a');
            if (link) {
                card.style.cursor = 'pointer';
                card.addEventListener('click', (e) => {
                    // Don't trigger if the actual link was clicked
                    if (e.target.tagName !== 'A' && !link.contains(e.target)) {
                        link.click();
                    }
                });
            }
        });
    };

    handleCardClick('.project-card');
    handleCardClick('.job-item');

    // --- Typing Animation for Hero Title ---
    const titleEl = document.querySelector('.hero-content .title');
    if (titleEl) {
        // Only run if not already set (reloading languages might mess it up otherwise, but text is static here)
        const text = "Utku Altan Özkale.";
        titleEl.textContent = '';
        let i = 0;
        const typeWriter = () => {
            if (i < text.length) {
                titleEl.textContent += text.charAt(i);
                i++;
                setTimeout(typeWriter, 100);
            }
        };
        setTimeout(typeWriter, 500);
    }

    // --- Custom Cursor Logic ---
    const dot = document.querySelector('.cursor-dot');
    const outline = document.querySelector('.cursor-outline');

    if (dot && outline) {
        window.addEventListener('mousemove', (e) => {
            const posX = e.clientX;
            const posY = e.clientY;
            
            dot.style.left = `${posX}px`;
            dot.style.top = `${posY}px`;

            if (typeof gsap !== 'undefined') {
                gsap.to(outline, {
                    left: posX,
                    top: posY,
                    duration: 0.15,
                    ease: "power2.out"
                });
            } else {
                outline.style.left = `${posX}px`;
                outline.style.top = `${posY}px`;
            }
        });

        const interactables = document.querySelectorAll('a, button, input, .project-card, .job-item, .lang-btn');
        interactables.forEach(link => {
            link.addEventListener('mouseenter', () => {
                if (typeof gsap !== 'undefined') {
                    gsap.to(outline, {
                        scale: 1.5,
                        backgroundColor: 'rgba(0, 243, 255, 0.1)',
                        duration: 0.3
                    });
                }
            });
            link.addEventListener('mouseleave', () => {
                if (typeof gsap !== 'undefined') {
                    gsap.to(outline, {
                        scale: 1,
                        backgroundColor: 'transparent',
                        duration: 0.3
                    });
                }
            });
        });
    }

    // --- Vanilla Tilt Initialization ---
    if (typeof VanillaTilt !== 'undefined') {
        VanillaTilt.init(document.querySelectorAll(".job-item, .project-card, .edu-item, .contact-content, .skills-list"), {
            max: 5,
            speed: 400,
            glare: true,
            "max-glare": 0.1,
            scale: 1.02
        });
    }

    // --- GSAP ScrollTrigger Initializations ---
    if (typeof gsap !== 'undefined' && typeof ScrollTrigger !== 'undefined') {
        gsap.registerPlugin(ScrollTrigger);
        
        gsap.utils.toArray('.reveal').forEach((section) => {
            gsap.from(section, {
                scrollTrigger: {
                    trigger: section,
                    start: "top 80%",
                    toggleActions: "play none none reverse"
                },
                y: 50,
                opacity: 0,
                duration: 1,
                ease: "power3.out"
            });
        });

        gsap.utils.toArray('.projects-grid').forEach(grid => {
            gsap.from(grid.querySelectorAll('.project-card'), {
                scrollTrigger: {
                    trigger: grid,
                    start: "top 85%",
                },
                y: 50,
                opacity: 0,
                duration: 0.8,
                stagger: 0.15,
                ease: "power2.out"
            });
        });

        gsap.utils.toArray('.jobs-tabs').forEach(grid => {
            gsap.from(grid.querySelectorAll('.job-item'), {
                scrollTrigger: {
                    trigger: grid,
                    start: "top 85%",
                },
                y: 30,
                opacity: 0,
                duration: 0.8,
                stagger: 0.15,
                ease: "power2.out"
            });
        });
    }

});
