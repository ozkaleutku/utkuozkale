document.addEventListener('DOMContentLoaded', () => {
    // Smooth scrolling
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            document.querySelector(this.getAttribute('href')).scrollIntoView({
                behavior: 'smooth'
            });
        });
    });

    // Mobile Menu
    const hamburger = document.querySelector('.hamburger');
    const navLinks = document.querySelector('.nav-links');

    if (hamburger) {
        hamburger.addEventListener('click', () => {
            if (navLinks.style.display === 'flex') {
                navLinks.style.display = 'none';
            } else {
                navLinks.style.display = 'flex';
                // Inline styles for basic toggle, can be improved with classes
                navLinks.style.flexDirection = 'column';
                navLinks.style.position = 'absolute';
                navLinks.style.top = '80px';
                navLinks.style.right = '0';
                navLinks.style.backgroundColor = 'rgba(10, 25, 47, 0.95)';
                navLinks.style.width = '100%';
                navLinks.style.padding = '20px';
                navLinks.style.textAlign = 'center';
            }
        });
    }

    // Force scroll to top on refresh
    if (history.scrollRestoration) {
        history.scrollRestoration = 'manual';
    } else {
        window.onbeforeunload = function () {
            window.scrollTo(0, 0);
        }
    }
    window.scrollTo(0, 0);

    // Language Toggle logic continues...
    let currentLang = localStorage.getItem('lang') || 'tr';
    const langBtn = document.getElementById('lang-toggle');

    // Initial load
    updateLanguage(currentLang);

    langBtn.addEventListener('click', () => {
        currentLang = currentLang === 'tr' ? 'en' : 'tr';
        updateLanguage(currentLang);
        localStorage.setItem('lang', currentLang);
    });

    function updateLanguage(lang) {
        // Update button text to show the *other* option or current? Usually current.
        // Let's make button show the target language "EN" if we are in TR, "TR" if we are in EN
        // Or just show current. Let's show "TR | EN" style or just content

        if (lang === 'tr') {
            langBtn.textContent = 'EN'; // Switch to EN
            document.documentElement.lang = 'tr';
        } else {
            langBtn.textContent = 'TR'; // Switch to TR
            document.documentElement.lang = 'en';
        }

        // Update all text elements
        document.querySelectorAll('[data-i18n]').forEach(element => {
            const key = element.getAttribute('data-i18n');
            if (translations[lang][key]) {
                element.textContent = translations[lang][key];
            }
        });
    }
});
